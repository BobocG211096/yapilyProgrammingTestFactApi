package uk.yapily.facts.model;

public class StatusFact {
    private int totalFacts;

    private long uniqueFacts;

    public int getTotalFacts() {
        return totalFacts;
    }

    public void setTotalFacts(int totalFacts) {
        this.totalFacts = totalFacts;
    }

    public long getUniqueFacts() {
        return uniqueFacts;
    }

    public void setUniqueFacts(long uniqueFacts) {
        this.uniqueFacts = uniqueFacts;
    }
}
